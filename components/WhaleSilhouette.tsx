import type { SpeciesKind } from "@/lib/species";

const W = 200;
const H = 80;

function buildPath(kind: SpeciesKind): string {
  const w = W;
  const h = H;
  if (kind === "sperm") {
    return [
      `M ${-w * 0.5} ${h * 0.05}`,
      `C ${-w * 0.35} ${-h * 0.35} ${w * 0.2} ${-h * 0.42} ${w * 0.38} ${-h * 0.18}`,
      `C ${w * 0.48} ${-h * 0.04} ${w * 0.48} ${h * 0.04} ${w * 0.38} ${h * 0.18}`,
      `C ${w * 0.2} ${h * 0.42} ${-w * 0.35} ${h * 0.35} ${-w * 0.5} ${h * 0.05}`,
      `Z`,
      `M ${w * 0.3} ${-h * 0.14} L ${w * 0.55} ${-h * 0.42} L ${w * 0.42} ${-h * 0.06} Z`,
      `M ${w * 0.3} ${h * 0.14} L ${w * 0.55} ${h * 0.42} L ${w * 0.42} ${h * 0.06} Z`,
    ].join(" ");
  }
  if (kind === "fin") {
    return [
      `M ${-w * 0.52} ${h * 0.08}`,
      `C ${-w * 0.38} ${-h * 0.38} ${w * 0.22} ${-h * 0.44} ${w * 0.4} ${-h * 0.16}`,
      `C ${w * 0.5} ${-h * 0.03} ${w * 0.5} ${h * 0.03} ${w * 0.4} ${h * 0.16}`,
      `C ${w * 0.22} ${h * 0.44} ${-w * 0.38} ${h * 0.38} ${-w * 0.52} ${h * 0.08}`,
      `Z`,
      `M ${w * 0.32} ${-h * 0.14} L ${w * 0.58} ${-h * 0.44} L ${w * 0.44} ${-h * 0.08} Z`,
      `M ${w * 0.32} ${h * 0.14} L ${w * 0.58} ${h * 0.44} L ${w * 0.44} ${h * 0.08} Z`,
      `M ${-w * 0.05} ${-h * 0.42} L ${w * 0.1} ${-h * 0.06} L ${w * 0.25} ${-h * 0.42} Z`,
    ].join(" ");
  }
  if (kind === "right") {
    return [
      `M ${-w * 0.5} 0`,
      `C ${-w * 0.32} ${-h * 0.5} ${w * 0.15} ${-h * 0.6} ${w * 0.38} ${-h * 0.2}`,
      `C ${w * 0.5} ${-h * 0.05} ${w * 0.5} ${h * 0.05} ${w * 0.38} ${h * 0.2}`,
      `C ${w * 0.15} ${h * 0.6} ${-w * 0.32} ${h * 0.5} ${-w * 0.5} 0`,
      `Z`,
      `M ${w * 0.28} ${-h * 0.16} L ${w * 0.55} ${-h * 0.52} L ${w * 0.4} ${-h * 0.08} Z`,
      `M ${w * 0.28} ${h * 0.16} L ${w * 0.55} ${h * 0.52} L ${w * 0.4} ${h * 0.08} Z`,
    ].join(" ");
  }
  if (kind === "humpback") {
    return [
      `M ${-w * 0.48} 0`,
      `C ${-w * 0.32} ${-h * 0.46} ${w * 0.18} ${-h * 0.5} ${w * 0.38} ${-h * 0.18}`,
      `C ${w * 0.5} ${-h * 0.04} ${w * 0.5} ${h * 0.04} ${w * 0.38} ${h * 0.18}`,
      `C ${w * 0.18} ${h * 0.5} ${-w * 0.32} ${h * 0.46} ${-w * 0.48} 0`,
      `Z`,
      `M ${w * 0.3} ${-h * 0.14} L ${w * 0.56} ${-h * 0.48} L ${w * 0.42} ${-h * 0.08} Z`,
      `M ${w * 0.3} ${h * 0.14} L ${w * 0.56} ${h * 0.48} L ${w * 0.42} ${h * 0.08} Z`,
      `M ${-w * 0.02} ${-h * 0.48} L ${w * 0.12} ${-h * 0.92} L ${w * 0.26} ${-h * 0.48} Z`,
    ].join(" ");
  }
  if (kind === "blue") {
    return [
      `M ${-w * 0.58} ${h * 0.06}`,
      `C ${-w * 0.4} ${-h * 0.36} ${w * 0.28} ${-h * 0.42} ${w * 0.46} ${-h * 0.14}`,
      `C ${w * 0.56} ${-h * 0.02} ${w * 0.56} ${h * 0.02} ${w * 0.46} ${h * 0.14}`,
      `C ${w * 0.28} ${h * 0.42} ${-w * 0.4} ${h * 0.36} ${-w * 0.58} ${h * 0.06}`,
      `Z`,
      `M ${w * 0.36} ${-h * 0.12} L ${w * 0.64} ${-h * 0.44} L ${w * 0.5} 0 L ${w * 0.64} ${h * 0.44} L ${w * 0.36} ${h * 0.12} Z`,
    ].join(" ");
  }
  return [
    `M ${-w * 0.44} ${h * 0.04}`,
    `C ${-w * 0.28} ${-h * 0.38} ${w * 0.2} ${-h * 0.42} ${w * 0.38} ${-h * 0.16}`,
    `C ${w * 0.48} ${-h * 0.03} ${w * 0.48} ${h * 0.03} ${w * 0.38} ${h * 0.16}`,
    `C ${w * 0.2} ${h * 0.42} ${-w * 0.28} ${h * 0.38} ${-w * 0.44} ${h * 0.04}`,
    `Z`,
    `M ${w * 0.28} ${-h * 0.14} L ${w * 0.52} ${-h * 0.46} L ${w * 0.4} ${-h * 0.08} Z`,
    `M ${w * 0.28} ${h * 0.14} L ${w * 0.52} ${h * 0.46} L ${w * 0.4} ${h * 0.08} Z`,
    `M ${-w * 0.06} ${-h * 0.42} L ${w * 0.08} ${-h * 0.92} L ${w * 0.24} ${-h * 0.42} Z`,
  ].join(" ");
}

export function WhaleSilhouette({
  kind,
  color,
  className,
  ariaLabel,
  opacity = 1,
}: {
  kind: SpeciesKind;
  color: string;
  className?: string;
  ariaLabel?: string;
  opacity?: number;
}) {
  return (
    <svg
      viewBox={`-${W / 2} -${H} ${W} ${H * 2}`}
      className={className}
      role={ariaLabel ? "img" : "presentation"}
      aria-label={ariaLabel}
      preserveAspectRatio="xMidYMid meet"
    >
      <path
        d={buildPath(kind)}
        fill={color}
        stroke={color}
        strokeWidth={0.6}
        opacity={opacity}
      />
    </svg>
  );
}
